def build_user_message(row_data):
    """1商品分のユーザーメッセージを構築"""
    parts = ["以下の商品データからメルカリ用のタイトルと商品説明文を生成してください。", ""]

    fields = [
        ("ブランド", row_data.get("brand", "")),
        ("AIキーワード（デザイン情報）", row_data.get("ai_keywords", "")),
        ("アイテム", row_data.get("item", "")),
        ("サイズ", row_data.get("size", "")),
        ("状態", row_data.get("condition", "")),
        ("傷汚れ詳細", row_data.get("damage_detail", "")),
        ("採寸情報", row_data.get("measurements", "")),
        ("販売価格", row_data.get("price", "")),
    ]

    for label, value in fields:
        v = str(value).strip() if value else ""
        parts.append(f"{label}: {v if v else '（なし）'}")

    return "\n".join(parts)
